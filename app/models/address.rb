class Address < ApplicationRecord
  validates :price, presence: true
  # 郵便番号が必須であること
  validates :postal_code
  # 都道府県が必須であること
  validates :prefecture
  # 市区町村が必須であること
  validates :municipality
  # 番地が必須であること
  validates :building_number
  # 電話番号が必須であること
  validates :address
  # 郵便番号にはハイフンが必要であること
  
end
